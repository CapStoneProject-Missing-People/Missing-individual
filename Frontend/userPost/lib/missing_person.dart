class MissingPerson {
  final String name;
  final int age;
  final String lastSeenPlace;
  final List<String> photos;
  final String phoneNumber;
  final String description;

  MissingPerson(
      {required this.name,
      required this.age,
      required this.lastSeenPlace,
      required this.photos,
      required this.phoneNumber,
      required this.description});
}
